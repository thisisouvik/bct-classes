#There are 4 tpes of numbers 

# integer, boolean, complex, float, string, list, tuple, set, dictionary

# integer
a = 1
print(type(a))

# boolean
b = True
print(type(b))

# complex
c = 1 + 2j
print(type(c))

# float
d = 1.0
print(type(d))

# string
e = "Hello"
print(type(e))

# list
f = [1, 2, 3]
print(type(f))

# tuple
g = (1, 2, 3)
print(type(g))

# set
h = {1, 2, 3}
print(type(h))

# dictionary
i = {1: "Hello", 2: "World"}
print(type(i))

# frozenset
j = frozenset({1, 2, 3})
print(type(j))

# bytes
k = b"Hello"
print(type(k))

# bytearray
l = bytearray(b"Hello")
print(type(l))

# memoryview
m = memoryview(b"Hello")
print(type(m))

